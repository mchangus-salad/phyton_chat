def _parse_positive_int(raw_value, default_value, minimum=0, maximum=200):
	try:
		value = int(raw_value)
	except (TypeError, ValueError):
		return default_value
	if value < minimum:
		return minimum
	if value > maximum:
		return maximum
	return value


def _make_title_from_content(content: str, max_len: int = 72) -> str:
	text = (content or '').strip().replace('\n', ' ')
	while '  ' in text:
		text = text.replace('  ', ' ')
	if not text:
		return 'New chat'
	for delimiter in ('. ', '? ', '! ', '; '):
		idx = text.find(delimiter)
		if idx > 0:
			text = text[:idx + 1]
			break
	title = text[:max_len].strip()
	return title or 'New chat'


def _make_search_snippet(text: str, term: str, max_len: int = 160) -> str:
	body = (text or '').strip()
	needle = (term or '').strip().lower()
	if not body:
		return ''
	if not needle:
		return body[:max_len]
	idx = body.lower().find(needle)
	if idx < 0:
		return body[:max_len]
	half = max_len // 2
	start = max(0, idx - half)
	end = min(len(body), start + max_len)
	if end - start < max_len:
		start = max(0, end - max_len)
	return body[start:end]


