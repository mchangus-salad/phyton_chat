def patient_case_analyze(request):
	"""
	Analyze a patient case with HIPAA-compliant PHI de-identification.

	Processing pipeline:
	1. Extract text from uploaded file or use provided free text.
	2. Detect and redact all 18 HIPAA Safe Harbor PHI categories.
	3. Build a structured clinical analysis prompt from de-identified text.
	4. Retrieve relevant medical evidence from the knowledge base.
	5. Generate evidence-based recommendations via the LLM.
	6. Persist an audit record (zero PHI stored).
	"""
	import hashlib
	import uuid as _uuid

	from .agent_ai.file_extractor import extract_text
	from .agent_ai.phi_deidentifier import deidentify
	from .models import PatientCaseSession

	serializer = PatientCaseUploadSerializer(data=request.data)
	if not serializer.is_valid():
		return _validation_error_response(serializer)

	payload = serializer.validated_data
	domain = (payload.get('domain') or 'medical').strip() or 'medical'
	subdomain = payload.get('subdomain') or None
	user_id = payload.get('user_id', 'anonymous')
	question = (payload.get('question') or '').strip()
	request_id = getattr(request, 'request_id', 'n/a')
	source_filename = ''

	# ── Step 1: Extract raw text ─────────────────────────────────────────────
	raw_text = ''
	if payload.get('file'):
		uploaded = payload['file']
		source_filename = uploaded.name
		try:
			raw_text = extract_text(uploaded.name, uploaded.read())
		except ValueError as exc:
			return Response(
				{'error': 'file extraction failed', 'detail': str(exc), 'request_id': request_id},
				status=status.HTTP_400_BAD_REQUEST,
			)
	else:
		raw_text = payload.get('text', '')

	if not raw_text.strip():
		return Response(
			{'error': 'no text content found in the provided input', 'request_id': request_id},
			status=status.HTTP_400_BAD_REQUEST,
		)

	# ── Step 2: HIPAA Safe Harbor de-identification ──────────────────────────
	clean_text, redaction_summary = deidentify(raw_text)
	logger.info(
		'phi_deidentify request_id=%s redactions=%d categories=%s',
		request_id,
		redaction_summary.total_redactions,
		list(redaction_summary.categories.keys()),
	)

	# ── Step 3: One-way hash of original text for audit (no PHI stored) ──────
	text_hash = hashlib.sha256(raw_text.encode('utf-8', errors='replace')).hexdigest()

	# ── Step 4: Build clinical analysis prompt ────────────────────────────────
	clinical_prompt = _build_case_prompt(clean_text, question, domain)

	# ── Step 5: Evidence-based analysis ──────────────────────────────────────
	try:
		service = _get_service(domain=domain, subdomain=subdomain)
		result = service.ask(question=clinical_prompt, user_id=user_id)
	except Exception:
		logger.exception('patient_case_analyze failed request_id=%s', request_id)
		return Response(
			{'error': 'analysis failed', 'request_id': request_id},
			status=status.HTTP_500_INTERNAL_SERVER_ERROR,
		)

	# ── Step 6: Persist audit record (zero PHI) ───────────────────────────────
	session_id = _uuid.uuid4()
	try:
		PatientCaseSession.objects.create(
			session_id=session_id,
			domain=domain,
			subdomain=subdomain or '',
			text_hash=text_hash,
			redaction_count=redaction_summary.total_redactions,
			redaction_categories=redaction_summary.categories,
			source_filename=source_filename,
			user_id=user_id,
		)
	except Exception:
		# Audit persistence failure must not surface to the caller.
		logger.exception('audit_log_failed request_id=%s session_id=%s', request_id, session_id)

	return Response(
		{
			'session_id': str(session_id),
			'analysis': result.answer,
			'citations': result.citations or [],
			'redaction_summary': redaction_summary.as_dict(),
			'domain': domain,
			'safety_notice': PATIENT_CASE_SAFETY_NOTICE,
			'request_id': request_id,
		},
		status=status.HTTP_200_OK,
	)


